let activeThreads = new Map();

module.exports.config = {
  name: "mojadar",
  version: "1.0.0",
  permission: 1,  // অ্যাডমিন চালাতে পারবে
  credits: "RiisHaD SoBuJ",
  description: "মজাদার বাংলা গালি মেনশন করে পাঠাবে",
  prefix: true,
  category: "fun",
  usages: "mojadar @mention",
  cooldowns: 5,
};

module.exports.run = async function ({ api, event, args }) {
  const { threadID, messageID, mentions, body, senderID } = event;

  // অ্যাডমিন চেক
  const threadInfo = await api.getThreadInfo(threadID);
  const isAdmin = threadInfo.adminIDs.some(admin => admin.id === senderID);
  if (!isAdmin) {
    return api.sendMessage("❌ ভাই, এই কমান্ড চালানোর অনুমতি নেই। শুধু অ্যাডমিন পারবেন।", threadID, messageID);
  }

  // স্টপ কমান্ড
  if (args[0] && args[0].toLowerCase() === "stop") {
    if (activeThreads.has(threadID)) {
      clearInterval(activeThreads.get(threadID));
      activeThreads.delete(threadID);
      return api.sendMessage("🛑 মজাদার গালি বন্ধ করা হলো!", threadID, messageID);
    } else {
      return api.sendMessage("⚠️ এখনো কোনো গালি পাঠানো হচ্ছে না।", threadID, messageID);
    }
  }

  if (!Object.keys(mentions).length) {
    return api.sendMessage("⚠️ দয়া করে কাউকে মেনশন করো।", threadID, messageID);
  }

  const mentionID = Object.keys(mentions)[0];
  const mentionName = mentions[mentionID];
  let count = 0;

  const interval = setInterval(() => {
    if (count >= mojadarGali.length) {
      clearInterval(interval);
      activeThreads.delete(threadID);
      return;
    }
    api.sendMessage(
      `@${mentionName} ${mojadarGali[count]}`,
      threadID,
      (err, info) => {
        if (err) console.log(err);
      },
      { mentions: [{ tag: mentionName, id: mentionID }] }
    );
    count++;
  }, 300);

  activeThreads.set(threadID, interval);
};
